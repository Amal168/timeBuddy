    final name = TextEditingController();
    final course = TextEditingController();
    final assignedTo = TextEditingController();
    DateTime? deadline;

    showDialog(
      context: context,
      builder: (_) => AlertDialog(
        title: const Text('Assign Task'),
        content: StatefulBuilder(
          builder: (ctx, setS) => SingleChildScrollView(
            child: Column(
              mainAxisSize: MainAxisSize.min,
              children: [
                TextField(
                  controller: name,
                  decoration: const InputDecoration(
                    labelText: 'Task name',
                    border: OutlineInputBorder(),
                  ),
                ),
                const SizedBox(height: 12),
                TextField(
                  controller: course,
                  decoration: const InputDecoration(
                    labelText: 'Course',
                    border: OutlineInputBorder(),
                  ),
                ),
                const SizedBox(height: 12),
                TextField(
                  controller: assignedTo,
                  decoration: const InputDecoration(
                    labelText: 'Assign to (student)',
                    border: OutlineInputBorder(),
                  ),
                ),
                const SizedBox(height: 12),
                ListTile(
                  contentPadding: EdgeInsets.zero,
                  title: Text(
                    deadline == null
                        ? 'Pick Deadline'
                        : DateFormat('MMM dd, yyyy').format(deadline!),
                  ),
                  trailing: const Icon(Icons.event),
                  onTap: () async {
                    final date = await showDatePicker(
                      context: ctx,
                      firstDate: DateTime.now(),
                      lastDate: DateTime.now().add(const Duration(days: 365)),
                      initialDate: DateTime.now().add(const Duration(days: 7)),
                    );
                    if (date != null) setS(() => deadline = date);
                  },
                ),
              ],
            ),
          ),
        ),
        actions: [
          TextButton(
              onPressed: () => Navigator.pop(context),
              child: const Text('Cancel')),
          FilledButton(
            onPressed: () async {
              if (name.text.isEmpty ||
                  course.text.isEmpty ||
                  assignedTo.text.isEmpty ||
                  deadline == null) {
                ScaffoldMessenger.of(context).showSnackBar(
                    const SnackBar(content: Text('Fill all fields')));
                return;
              }
              await fireRepo.addTask(
                Task(
                  id: 'tmp',
                  name: name.text.trim(),
                  courseName: course.text.trim(),
                  assignedTo: assignedTo.text.trim(),
                  deadline: deadline!,
                ),
              );
              await fireRepo.addNotification(
                AppNotification(
                  id: 'tmp',
                  title: 'Task Assigned',
                  message:
                      '${assignedTo.text.trim()} → ${name.text.trim()} (${course.text.trim()})',
                  type: NotificationType.taskAssigned,
                  timestamp: DateTime.now(),
                ),
              );
              if (!mounted) return;
              Navigator.pop(context);
            },
            child: const Text('Assign'),
          ),
        ],
      ),
    );
  }

  BoxDecoration _cardDeco() => BoxDecoration(
